TRANSLATIONS: Mapping[str, Mapping[str, str]] = {
    "en": _EN,
    "cz": _CZ,
}


def normalize_lang(lang: str | None) -> str:
    lang = (lang or "").strip().lower()
    if lang == "cs":
        lang = "cz"
    return lang if lang in SUPPORTED_LANGS else "ru"


def html_lang_code(lang: str | None) -> str:
    """BCP 47 для <html lang> и полей даты (cz → cs)."""
    return {"ru": "ru", "en": "en", "cz": "cs"}.get(normalize_lang(lang), "ru")


def detect_lang_from_accept_language(header: str | None) -> str:
    """Язык из заголовка Accept-Language браузера."""
    if not header:
        return "ru"
    for item in header.split(","):
        token = item.strip().split(";")[0].lower().replace("_", "-")
        if not token:
            continue
        primary = token.split("-")[0]
        if primary in SUPPORTED_LANGS or primary == "cs":
            return normalize_lang(primary)
    return "ru"


def resolve_lang(cookie_lang: str | None, accept_language: str | None) -> str:
    """Cookie (явный выбор) важнее языка браузера."""
    if cookie_lang is not None and str(cookie_lang).strip():
        return normalize_lang(cookie_lang)
    return detect_lang_from_accept_language(accept_language)


def translate(lang: str, text: str) -> str:
    if not text:
        return text
    l = normalize_lang(lang)
    if l == "ru":
        return text
    return TRANSLATIONS.get(l, {}).get(text, text)


def translate_payment_label(lang: str, label: str) -> str:
    if not label or label == "—":
        return label
    if label in ("Оплачен", "Не оплачен"):
        return translate(lang, label)
    if label.startswith("Частично ("):
        inner = label[len("Частично (") :]
        return f"{translate(lang, 'Частично')} ({inner}"
    return label


def translate_stored_note(lang: str, text: str | None) -> str:
    """Переводит сохранённые в БД заметки с известными русскими фразами."""
    if not text:
        return text or ""
    if normalize_lang(lang) == "ru":
        return text
    translated = translate(lang, text)
    if translated != text:
        return translated
    cost_prefix = "Стоимость: "
    if text.startswith(cost_prefix):
        return f"{translate(lang, 'Стоимость:')} {text[len(cost_prefix):]}"
    return text


def translate_named(lang: str, template: str, **kwargs: str) -> str:
    """Перевод шаблона с подстановкой переведённых значений."""
    tpl = translate(lang, template)
    return tpl.format(**{k: translate(lang, v) for k, v in kwargs.items()})


def translate_outbound_message(lang: str, msg: str) -> str:
    """Перевод системных сообщений отправки (Telegram/WhatsApp)."""
    if not msg:
        return msg
    translated = translate(lang, msg)
    if translated != msg:
        return translated
    for prefix in ("WhatsApp API: ", "Ошибка сети: "):
        if msg.startswith(prefix):
            key = prefix.rstrip()
            return f"{translate(lang, key)} {msg[len(prefix):]}"
    return msg


